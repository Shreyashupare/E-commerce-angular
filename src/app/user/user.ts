export class user{
    firstname:string;
    lastname:string;
    username:string;
    email:string;
    dob:string;
    constructor(firstname:string, lastname:string, username:string, email:string, dob:string){
        this.firstname = firstname;
        this.lastname = lastname;
        this.username = username;
        this.email = email;
        this.dob = dob;

    }
}